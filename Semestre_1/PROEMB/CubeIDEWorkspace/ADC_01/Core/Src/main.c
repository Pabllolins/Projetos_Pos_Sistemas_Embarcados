//Função de inicialização do ADC1 e EA0 (PA0)
//modo polling (one-shot), APB2 (PCLK2) = 42 MHz
void ADC1_Init(void)
{
	uint32_t tmpreg = 0;
	//Configurar os GPIOs

	//Ativar o clock
	RCC->AHB1ENR |= RCC_AHB1ENR_GPIOAEN;

	//Aguardar o clock estabilizar
	tmpreg = RCC->AHB1ENR;

	//Definir o modo de funcionamento analógico (11)
	GPIOA->MODER |= GPIO_MODER_MODE0;

	//Desativar pullup e pulldown
	GPIOA->PUPDR &= ~GPIO_PUPDR_PUPD0;

	//Configurar o ADC1
	//Ativar o clock
	RCC->APB2ENR |= RCC_APB2ENR_ADC1EN;

	//Aguardar o clock estabilizar
	tmpreg = RCC->APB2ENR;

	//Desabilitar o ADC1
	ADC1->CR2 &= ~ADC_CR2_ADON;

	//Programar a resolução (00 = 12 bits)
	ADC1->CR1 &= ~ADC_CR1_RES;

	//Ajustar o alinhamento do resultado (0 = direita)
	ADC1->CR2 &= ~ADC_CR2_ALIGN;

	//Habilitar sinalização do bit EOC no final de uma conversão
	ADC1->CR2 |= ADC_CR2_EOCS;

	//Ajustar o tempo de conversão do canal 0 (001 = 15 ciclos de
	//clock)
	ADC1->SMPR2 |= ADC_SMPR2_SMP0_0;

	//Definir a sequência regular de conversão (apenas 1 conversão, canal 0)
	ADC1->SQR1 &= ~ADC_SQR1_L_Msk; //Limpa os bits = 1 conversão
	ADC1->SQR3 &= ~ADC_SQR3_SQ1_Msk;//Limpa os bits = canal 0

	//Ajuste o prescaler do ADC (01 = div 4)
	ADC->CCR |= ADC_CCR_ADCPRE_0;

	//Habilitar o ADC1
	ADC1->CR2 |= ADC_CR2_ADON;
}

